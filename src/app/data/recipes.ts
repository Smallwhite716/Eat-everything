// 根据食材匹配菜谱
export function matchRecipes(ingredients: string[]): Recipe[] {
  if (ingredients.length === 0) {
    return allRecipes;
  }

  const lowerIngredients = ingredients.map(i => i.toLowerCase());

  return allRecipes
    .map(recipe => {
      // 检查用户输入的食材是否匹配菜谱的主要食材
      const matched: string[] = [];
      const missing: string[] = [];

      recipe.mainIngredients.forEach(mainIng => {
        const found = lowerIngredients.some(userIng =>
          userIng.includes(mainIng.toLowerCase()) || mainIng.toLowerCase().includes(userIng)
        );
        if (found) {
          matched.push(mainIng);
        } else {
          missing.push(mainIng);
        }
      });

      return {
        ...recipe,
        matchInfo: {
          matched: matched.length,
          total: recipe.mainIngredients.length,
          missing
        }
      };
    })
    .filter(recipe => recipe.matchInfo && recipe.matchInfo.matched > 0)
    .sort((a, b) => (b.matchInfo?.matched || 0) - (a.matchInfo?.matched || 0));
}

// 根据分类过滤菜谱
export function filterByCategory(recipes: Recipe[], category: string): Recipe[] {
  if (category === '全部') {
    return recipes;
  }
  return recipes.filter(recipe => recipe.category === category);
}
